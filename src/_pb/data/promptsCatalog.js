// שולף placeholders ייחודיים מתוך תבנית פרומפט, לפי סדר הופעתם
export function extractPlaceholders(template) {
  const matches = template.match(/\[([^\]]+)\]/g) || [];
  const seen = new Set();
  const result = [];
  for (const raw of matches) {
    const label = raw.slice(1, -1);
    if (!seen.has(label)) {
      seen.add(label);
      result.push(label);
    }
  }
  return result;
}
